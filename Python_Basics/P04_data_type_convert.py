# int(x, [, base]), specify the x's base
num1 = int("12", 16)
print(num1)

# str(x), repr(x)
str1 = "hello world"
print(str(str1))
print(repr(str1))

# eval(x), compute the string x
print(eval("2+3"))

# ord(x), compute the ascii code of one character
print(ord('1'))
# chr(x), convert int to char
print(chr(49))